onMounted(() => {
  const easing = 'cubic-bezier(.16,1,.3,1)'
  const chinaCols = Array.from(document.querySelectorAll<HTMLElement>('#chinaSec .bar .col'))
  chinaCols.forEach(c => {
    c.style.flexGrow = '0'
    c.style.flexShrink = '0'
    c.style.height = '0px'
  })

  const prog = document.getElementById('prog')
  function onScroll() {
    if (!prog) return
    const h = document.documentElement
    prog.style.width = (h.scrollTop / (h.scrollHeight - h.clientHeight) * 100) + '%'
  }
  onScroll()

  const rail = document.getElementById('rail')
  const links: HTMLAnchorElement[] = rail ? Array.from(rail.querySelectorAll('a')) : []
  function setActive() {
    if (!links.length) return
    let cur = links[0].dataset.t || ''
    links.forEach(a => {
      const el = a.dataset.t ? document.getElementById(a.dataset.t) : null
      if (el && el.getBoundingClientRect().top <= window.innerHeight * 0.45) cur = a.dataset.t || cur
    })
    links.forEach(a => a.classList.toggle('on', a.dataset.t === cur))
  }
  let ticking = false
  window.addEventListener('scroll', () => {
    if (!ticking) {
      ticking = true
      requestAnimationFrame(() => { onScroll(); setActive(); ticking = false })
    }
  }, { passive: true })
  setActive()

  const reduce = window.matchMedia('(prefers-reduced-motion: reduce)').matches
  function ease(k: number) { return 1 - Math.pow(1 - k, 3) }
  function animate(dur: number, fn: (e: number) => void) {
    if (reduce) { fn(1); return }
    const t0 = performance.now()
    function s(t: number) { const k = Math.min((t - t0) / dur, 1); fn(ease(k)); if (k < 1) requestAnimationFrame(s) }
    requestAnimationFrame(s)
  }
  function countInt(el: HTMLElement, target: number, dur: number) {
    animate(dur, e => { el.textContent = 'US$ ' + Math.round(target * e).toLocaleString('pt-BR') })
  }
  function countBi(el: HTMLElement, target: number, dur: number) {
    animate(dur, e => { el.textContent = 'US$ ' + (target * e).toFixed(2).replace('.', ',') + ' bi' })
  }
  function countDec(el: HTMLElement, target: number, dur: number) {
    animate(dur, e => { el.textContent = 'US$ ' + (target * e).toFixed(1).replace('.', ',') + ' bi' })
  }
  function countNum(el: HTMLElement, target: number, dur: number) {
    animate(dur, e => { el.textContent = Math.round(target * e).toLocaleString('pt-BR') })
  }
  function countPct(el: HTMLElement, target: number, dur: number) {
    animate(dur, e => { el.textContent = (target * e).toFixed(1).replace('.', ',') + '%' })
  }

  const io = new IntersectionObserver(es => {
    es.forEach(ent => {
      if (!ent.isIntersecting) return
      ent.target.classList.add('in')
      if (ent.target.id === 'trilSec') {
        const bn = document.getElementById('bignum'); if (bn) countInt(bn, 1141000000000, 1800)
        const rf = document.getElementById('ringfill') as SVGCircleElement | null; if (rf) rf.style.strokeDashoffset = '335.5'
        const rp = document.getElementById('ringpct'); if (rp) countPct(rp, 46.6, 1800)
      }
      if (ent.target.id === 'euaSec') {
        const un = document.getElementById('usaNum'); if (un) countDec(un, 244.7, 1500)
      }
      if (ent.target.id === 'potSec') {
        ent.target.querySelectorAll<HTMLElement>('.rk__bar i').forEach(b => { b.style.width = (b.dataset.w || '0') + '%' })
        ent.target.querySelectorAll<HTMLElement>('.rk__v[data-to]').forEach(el => countNum(el, parseInt(el.dataset.to || '0', 10), 1300))
      }
      if (ent.target.id === 'chinaSec') {
        ent.target.querySelectorAll<HTMLElement>('.vn[data-to]').forEach(el => countBi(el, parseFloat(el.dataset.to || '0'), 1300))
        chinaCols.forEach(c => {
          const h = parseInt(c.dataset.h || '0', 10)
          const anim = c.animate(
            [{ height: '0px' }, { height: h + 'px' }],
            { duration: 1300, easing, fill: 'forwards' }
          )
          anim.addEventListener('finish', () => { c.style.height = h + 'px' })
        })
      }
      io.unobserve(ent.target)
    })
  }, { threshold: .25 })
  document.querySelectorAll('.reveal').forEach(el => io.observe(el))

  const dd = document.getElementById('dd')
  if (dd) {
    const btn = document.getElementById('ddBtn')!
    const panel = document.getElementById('ddPanel')!
    const lbl = document.getElementById('ddLabel')!
    const hid = document.getElementById('perfilv') as HTMLInputElement
    btn.addEventListener('click', e => {
      e.stopPropagation()
      dd.classList.toggle('open')
      btn.setAttribute('aria-expanded', String(dd.classList.contains('open')))
    })
    panel.querySelectorAll<HTMLElement>('.dd__opt').forEach(o => {
      o.addEventListener('click', () => {
        hid.value = o.dataset.v || ''
        lbl.textContent = o.textContent
        lbl.style.color = '#fff'
        panel.querySelectorAll('.dd__opt').forEach(x => x.classList.remove('sel'))
        o.classList.add('sel')
        dd.classList.remove('open')
        btn.setAttribute('aria-expanded', 'false')
      })
    })
    document.addEventListener('click', e => { if (!dd.contains(e.target as Node)) dd.classList.remove('open') })
  }

  const teseForm = document.getElementById('teseForm') as HTMLFormElement | null
  if (teseForm) {
    teseForm.addEventListener('submit', e => {
      e.preventDefault()
